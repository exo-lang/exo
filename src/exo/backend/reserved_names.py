def is_exo_reserved_name(nm):
    nm = str(nm)
    nm_lower = nm.lower()
    for prefix in ("exo_", "_exo_", "__"):
        if nm_lower.startswith(prefix):
            return True
    if len(nm) >= 2 and nm[0] == "_" and "A" <= nm[1] <= "Z":
        return True
    return nm in _reserved_names


